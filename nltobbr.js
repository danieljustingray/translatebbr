// Function to translate English to Burenburgish
function translateToBurenburgish() {
    const englishText = document.getElementById('englishText').value.toLowerCase();
    const words = englishText.split(' ');

    // Translate word by word
    const translatedWords = words.map(word => {
        return dictionary[word] || word;  // If word exists in dictionary, translate; otherwise, keep it the same
    });

    // Join the translated words and show in the Burenburgish textarea
    document.getElementById('burenburgishText').value = translatedWords.join(' ');
}
